//set string or word to check
let string = "hello";
//define count for vowels and consonants
let vowels = 0;
let consonants = 0;
//for loop code to scan string  or word length
for (let i = 0; i < string.length; i++) {
  let char = string[i].toLowerCase();
  //if any characters are a,e,i,o,u, count as vowels
  if (
    char === "a" ||
    char === "e" ||
    char === "i" ||
    char === "o" ||
    char === "u"
  ) {
    vowels++;
    //else count all other letters as consonants
  } else if (char >= "a" && char <= "z") {
    consonants++;
  }
}
//print string with counts of vowels and consonants
console.log(
  string + " has " + consonants + " consonants and " + vowels + " vowels."
);

